public class ReadDo {
    private int readdoId;
    private int readdoNum;
    private String readdoSelect;
    private int read_pageId;
    private int userId;

    public int getReaddoId() {
        return readdoId;
    }

    public void setReaddoId(int readdoId) {
        this.readdoId = readdoId;
    }

    public int getReaddoNum() {
        return readdoNum;
    }

    public void setReaddoNum(int readdoNum) {
        this.readdoNum = readdoNum;
    }

    public String getReaddoSelect() {
        return readdoSelect;
    }

    public void setReaddoSelect(String readdoSelect) {
        this.readdoSelect = readdoSelect;
    }

    public int getRead_pageId() {
        return read_pageId;
    }

    public void setRead_pageId(int read_pageId) {
        this.read_pageId = read_pageId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }
}
