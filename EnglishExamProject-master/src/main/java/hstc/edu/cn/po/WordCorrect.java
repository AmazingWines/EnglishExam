public class WordCorrect {
    private int wordCorrectId;
    private int wordCorrectNum;
    private String wordCorrect;
    private int word_pageId;

    public int getWordCorrectId() {
        return wordCorrectId;
    }

    public void setWordCorrectId(int wordCorrectId) {
        this.wordCorrectId = wordCorrectId;
    }

    public int getWordCorrectNum() {
        return wordCorrectNum;
    }

    public void setWordCorrectNum(int wordCorrectNum) {
        this.wordCorrectNum = wordCorrectNum;
    }

    public String getWordCorrect() {
        return wordCorrect;
    }

    public void setWordCorrect(String wordCorrect) {
        this.wordCorrect = wordCorrect;
    }

    public int getWord_pageId() {
        return word_pageId;
    }

    public void setWord_pageId(int word_pageId) {
        this.word_pageId = word_pageId;
    }
}
